public class SeriesTask1 {
    public static void main(String[] args) {
        for (int i=0;i<26;i++){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=5;i<19;i++){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=30;i<51;i++){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=12;i>0;i--){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=100;i>84;i--){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=5;i<71;i=i+5){
            System.out.print(i+" ");
        }

        System.out.println("\n\n###############################\n");

        for (int i=0;i<12;i++){
            System.out.print("<> ");
        }
        System.out.println(" \n");

        for (int i=10;i>0;i--){
            System.out.print(i+", ");
        }
        System.out.println("0...BLAST OFF!\n");

        for (int i=0;i<26;i++){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=0;i<9;i++){
            System.out.print("1 0 ");
        }
        System.out.println(" \n");

        for (int i=1;i<9;i++){
            System.out.print(i+"/"+(i+1)+" ");
        }
    }
}