public class SeriesTask1 {
    public static void main(String[] args) {
        for (int i=0;i<26;i++){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=5;i<19;i++){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=30;i<51;i++){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=12;i>0;i--){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=100;i>84;i--){
            System.out.print(i+" ");
        }
        System.out.println(" \n");

        for (int i=5;i<71;i=i+5){
            System.out.print(i+" ");
        }
        System.out.println(" ");
    }
}
